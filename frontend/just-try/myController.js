function roundMinutesByStep15(date) {
    var actualMinutes = date.getMinutes();
    if(actualMinutes> 0 && actualMinutes<= 15) {
        date.setMinutes(15);
    }else if(actualMinutes> 15 && actualMinutes<=30){
        date.setMinutes(30);
    }else if(actualMinutes>30 && actualMinutes<=45){
        date.setMinutes(45);
    }else{
        date.setMinutes(0);
    }

    console.log("the rounded date is : " + date.toLocaleString());

    return date;
}
